package logical_program;

public class factorial2 
{
   public static void main(String[] args) 
   {
	 int num=5;
	 int fact=1;
	   
	         
	 for(int i=num; i>=1; i--)
	 {
		fact=fact*i;     //1
	 }
	   
	  System.out.println(fact); 
	   
	   
	   
	   
	   
	   
	   
	   
    }		
}
