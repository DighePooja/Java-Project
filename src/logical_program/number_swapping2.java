package logical_program;

public class number_swapping2 
{
public static void main(String[] args) 
{
   int a=10;
   int b=90;
   
   System.out.println(" Before Swapping: "+"a="+a +" b="+b);
   int c=a;
   a=b;
   b=c;
   
   System.out.println(" After Swapping: "+"a="+a +" b="+b);
   
}
}
