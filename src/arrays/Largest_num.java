package arrays;

public class Largest_num 
{
   public static void main(String[] args) 
   {
	   int[] ar= {10,20,50,90,9};
	   
	   int max=ar[0];
	   
	   for(int i=0; i<ar.length; i++)
	   {
		    
			if(ar[i]>max)   
			
			max=ar[i];
	   }
	   System.out.println("largest number: "+max);	
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
   }	

}
