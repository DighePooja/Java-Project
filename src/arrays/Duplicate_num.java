package arrays;

public class Duplicate_num 
{
public static void main(String[] args) 
{
     int [] ar= {10,20,90,77,60,90};
     
     for(int i=0; i<ar.length; i++)
     {
    	 for(int j=i+1; j<ar.length; j++)
    	  if(ar[i]==ar[j])
          System.out.println("Duplicate number is:"+ar[i]);	 
     }
     

	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
}
