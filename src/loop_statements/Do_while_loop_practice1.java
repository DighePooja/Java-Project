package loop_statements;

public class Do_while_loop_practice1 {
	
	public static void main(String[] args) 
      {
		int i=50;
		do
		{
			System.out.println(i);
			i=i+5;
		}
		while(i<=100);
		
		int a=20;
		do
		{
			
			System.out.println(a);
			a++;
		}
		while(a<=30);
	  }
	
	

}
