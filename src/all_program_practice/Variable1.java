package all_program_practice;

public class Variable1 
{
public static void main(String[] args) 
   {
	int num;
	String name;
	float per;
	double digit;                         ///variable declaration
	short pinnum;
	char grade;
	
	
	
	num= 1234567890;
	name= "Pooja";
	per= 95.55f;
	digit= 99.9999999d;                  //Variable initialisation
	pinnum= 1712;
	grade= 'a';
	
	System.out.println(num+"\n"+name+"\n"+per+"\n"+digit+"\n"+pinnum+"\n"+grade);
		
	
	
	
   }
}
